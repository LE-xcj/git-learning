package com.xc.gituse.entity;

/**
 * @author chujian
 * @ClassName User
 * @Description 功能描述
 * @date 2019/6/13 17:22
 */
public class User {
    private Integer id;
    private String name;
    private int age;
    private String sex;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }
}
    